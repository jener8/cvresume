export type TranslationKeys = keyof typeof translations.en

export function getTranslation(lang: Language, key: TranslationKeys): string {
  return translations[lang][key] || translations.en[key] || key
}

export function getFitScoreTypeLabel(lang: Language, type: "culture" | "ambitions" | "skills" | "strategy"): string {
  const labels = {
    culture: lang === "de" ? "Kultur" : "Culture",
    ambitions: lang === "de" ? "Ambitionen" : "Ambitions",
    skills: lang === "de" ? "Fähigkeiten" : "Skills",
    strategy: lang === "de" ? "Strategie" : "Strategy",
  }
  return labels[type]
}

export function getStatusLabel(lang: Language, status: string): string {
  const statusMap: Record<string, { en: string; de: string }> = {
    applied: { en: "Applied", de: "Beworben" },
    interview: { en: "Interview", de: "Vorstellungsgespräch" },
    offer: { en: "Offer", de: "Angebot" },
    rejected: { en: "Rejected", de: "Abgelehnt" },
    withdrawn: { en: "Withdrawn", de: "Zurückgezogen" },
  }
  return statusMap[status]?.[lang] || status
}
